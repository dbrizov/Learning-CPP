#include "ArithmeticOperators.h"

float ArithmeticOperators::SumNumbers(float firstNum, float secondNum)
{
	return firstNum + secondNum;
}

float ArithmeticOperators::DifferenceNumbers(float firstNum, float secondNum)
{
	return firstNum - secondNum;
}

float ArithmeticOperators::MultiplyNumbers(float firstNum, float secondNum)
{
	return firstNum * secondNum;
}

float ArithmeticOperators::DivideNumbers(float firstNum, float secondNum)
{
	return firstNum / secondNum;
}